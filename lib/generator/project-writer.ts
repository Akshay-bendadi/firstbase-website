export async function writeGeneratedProject(
  projectPath: string,
  answers: FirstbaseAnswers,
): Promise<void> {
  await rm(projectPath, { recursive: true, force: true });
  await mkdir(projectPath, { recursive: true });

  await write(projectPath, "package.json", packageJson(answers));
  await write(projectPath, "README.md", readme(answers));
  await write(projectPath, "docs/production-setup.md", docs(answers));
  await write(projectPath, ".gitignore", "node_modules\n.next\ndist\nbuild\n.env\n.env.local\n");
  await write(projectPath, ".env.example", "NEXT_PUBLIC_API_URL=https://api.example.com\n");
  await write(
    projectPath,
    "tailwind.config.ts",
    `export default { content: ["./**/*.{ts,tsx,js,jsx}"], theme: { extend: {} }, plugins: [] };\n`,
  );
  await write(
    projectPath,
    "postcss.config.cjs",
    `module.exports = { plugins: { tailwindcss: {}, autoprefixer: {} } };\n`,
  );
  await write(
    projectPath,
    answers.language === "ts" ? "lib/api.ts" : "lib/api.js",
    `import axios from "axios";\n\nexport const api = axios.create({ baseURL: process.env.NEXT_PUBLIC_API_URL, timeout: 15000 });\n`,
  );

  if (answers.framework === "next") {
    await write(
      projectPath,
      answers.language === "ts" ? "app/page.tsx" : "app/page.jsx",
      appSource(answers),
    );
    await write(
      projectPath,
      "app/globals.css",
      "@tailwind base;\n@tailwind components;\n@tailwind utilities;\n",
    );
    await write(
      projectPath,
      answers.language === "ts" ? "app/layout.tsx" : "app/layout.jsx",
      answers.language === "ts"
        ? `import "./globals.css";\n\nexport default function RootLayout({ children }: { children: React.ReactNode }) {\n  return <html lang="en"><body>{children}</body></html>;\n}\n`
        : `import "./globals.css";\n\nexport default function RootLayout({ children }) {\n  return <html lang="en"><body>{children}</body></html>;\n}\n`,
    );
    return;
  }

  const mainFile = answers.language === "ts" ? "main.tsx" : "main.jsx";
  await write(
    projectPath,
    "index.html",
    `<div id="root"></div><script type="module" src="/src/${mainFile}"></script>\n`,
  );
  await write(
    projectPath,
    answers.language === "ts" ? "src/App.tsx" : "src/App.jsx",
    viteSource(answers),
  );
  await write(
    projectPath,
    `src/${mainFile}`,
    answers.language === "ts"
      ? `import { createRoot } from "react-dom/client";\nimport { App } from "./App";\n\ncreateRoot(document.getElementById("root")!).render(<App />);\n`
      : `import { createRoot } from "react-dom/client";\nimport { App } from "./App";\n\ncreateRoot(document.getElementById("root")).render(<App />);\n`,
  );
  await write(
    projectPath,
    "src/index.css",
    "@tailwind base;\n@tailwind components;\n@tailwind utilities;\n",
  );
}
